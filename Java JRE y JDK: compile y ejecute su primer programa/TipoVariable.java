
public class TipoVariable {
	public static void main(String[] args) {
		System.out.println("Hello World");

		//Esta es una variable de tipo entero. 
		int edad = 25;
		System.out.println("Mi edad es " + edad);
		
		// Este es un tipo de datos double
		double salario = 1250.50;
		System.out.println("El salario es $" + salario);
		double salarioMitad = salario / 2;
		System.out.println(salarioMitad);
		//Ejemplo de double con divisi√≥n
		double division = 1250 / 3;
		System.out.println(division);

	}
}
